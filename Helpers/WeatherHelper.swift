import Foundation

enum WeatherType : String {
    case clear = "Clear"
    case clouds = "Clouds"
    case rain = "Rain"
    case thunderstorm = "Thunderstorm"
    case drizzle = "Drizzle"
    case snow = "Snow"
    case haze = "Haze"
    case smoke = "Smoke"
    case fog = "Fog"
    case mist = "Mist"
}

enum TemperatureRange : Double {
    case highTemp = 90.0
    case lowTemp = 60.0
    case coldTem = 32.0
}
